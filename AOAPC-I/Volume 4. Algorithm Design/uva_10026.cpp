struct Record{
  int T;
  int S;
  int idx;
};
Record records[1024];


bool operator<(const Record &a, const Record &b){
  return a.S * b.T > a.T * b.S;
}


int main(){
  int testNum;
  cin >> testNum;
  while (testNum--){
    int n;
    cin >> n;
    for (int i = 0; i < n; i++) {
      cin >> records[i].T >> records[i].S;
      records[i].idx = i + 1;
    }
    sort(records, records + n);
    for (int i = 0; i < n; i++){
      if (i != 0){
        printf(" ");
      }
      printf("%d", records[i].idx);
    }
    printf("\n");
    if (testNum) printf("\n");
  }
}
