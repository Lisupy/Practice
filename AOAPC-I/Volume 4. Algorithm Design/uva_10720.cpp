const int MAXN =  10 * 1024;
int n;
int a[MAXN];
bool isValid(){
  sort(a, a + n);
  while (n--){
    for (int i = n - 1; i >= 0 && a[n]; i--){
      if (a[i] < 0){
        return false;
      }else{
        a[i]--;
        a[n]--;
      }
    }
    if (a[n] > 0){
      return false;
    }
    sort(a, a + n);
  }
  return a[0] == 0;
}
int main(){
  while (scanf("%d", &n) && n){
    for (int i = 0; i < n; i++){
      scanf("%d", &a[i]);
    }
    if (isValid()){
      cout << "Possible" << endl;
    }else{
      cout << "Not possible" << endl;
    }
  }
}

