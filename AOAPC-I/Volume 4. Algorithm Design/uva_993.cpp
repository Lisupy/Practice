void solve(int N){
  if (N < 10){
    cout << N << endl;
    return;
  }
  string s;
  for (int i = 9; i > 1; i--){
    while (N % i == 0){
      s += i + '0';
      N /= i;
    }
  }
  if (N != 1){
    cout << -1 << endl;
  }else{
    sort(s.begin(), s.end());
    cout << s << endl;
  }
}
int main(){
  int TestNum;
  cin >> TestNum;
  while (TestNum--){
    int N;
    cin >> N;
    solve(N);
  }
}
