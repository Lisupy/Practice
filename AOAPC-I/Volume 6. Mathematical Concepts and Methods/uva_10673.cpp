int main(){
  long long T; cin >> T;
  while (T--){
    unsigned long long x, k; cin >> x >> k;
    unsigned long long px = floor((1.0 * x) / k);
    unsigned long long qx = ceil((1.0 * x) / k);
    unsigned long long p, q;
    //cout << px << endl;
    //cout << qx << endl;
    for (int i = 0; ; i++){
      p = i;
      q = (x - p * px) / qx;
      if (x == p * px + q * qx){
        break;
      }
    }
    cout << p << " " << q << endl;
  }
}
