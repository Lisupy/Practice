/*
 * __builtin_ffs  __builtin_clz  __builtin_ctz __builtin_popcount  __builtin_parity
 * sizeof CLOCKS_PER_SEC
 * (1 << (31 - __builtin_clz(100) ) == 64;
 */
const i64 MOD = 1000*1000;
i64 cache[200][200];

i64 C(int n, int k){
  if (n == 0) return k == 0;
  if (k == 0) return 1;
  i64 &res = cache[n][k];
  if (res != -1) return res;
  res = (C(n - 1, k) + C(n - 1, k - 1) )% MOD;
  return res;
}


