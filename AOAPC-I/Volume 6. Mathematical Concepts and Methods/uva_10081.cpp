double dp[128][10];
double solve(int k, int n){
  double f = 1.0 / (k + 1);
  memset(dp, 0, sizeof(dp));
  for (int i = 0; i <= k; i++) dp[1][i] = f;
  for (int j = 2; j <= n; j++){
    for (int i = 0; i <= k; i++){
      for (int i1 = max(0, i - 1); i1 <= min(k, i + 1); i1++){
        dp[j][i] += dp[j - 1][i1] * f;
      }
    }
  }
  double total = 0;
  for (int i = 0; i <= k; i++) total += dp[n][i];
  return total * 100;
}
