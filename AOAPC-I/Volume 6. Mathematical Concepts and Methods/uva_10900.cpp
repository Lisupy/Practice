double f_cache[32];
bool visited[32];
double f(int n, double p){
  double &res = f_cache[n];
  if (visited[n]) return res;
  visited[n] = true;
  if (n == 0) res = 1;
  else{
    double f1 = f(n-1, p);
    double x = 0.5/f1;
    if (p >= x) res = f1 * (p + 1);
    else res = f1 * (x + 1) * (1-x)/(1-p) + (x-p)/(1-p);
  }
  return res;
}
int main(){
  int n;
  double t;
  while (cin >> n >> t && n){
    memset(visited, 0, sizeof(visited));
    printf("%.03lf\n", f(n, t));
  }
}

