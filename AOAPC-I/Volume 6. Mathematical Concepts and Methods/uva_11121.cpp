void dfs(long long n){
  if (n == 0)return;
  int t = abs(n % 2);
  dfs((n - t) / -2);
  cout << t;
}
int main(){
  //cout << 7 / -2 << endl;
  //cout << 7 % -2 << endl;
  int N; cin >> N;
  for (int ti = 1; ti <= N; ti++){
    printf("Case #%d: ", ti);
    long long n; cin >> n;
    if (n == 0) cout << "0";
    else dfs(n);
    printf("\n");
  }
}
