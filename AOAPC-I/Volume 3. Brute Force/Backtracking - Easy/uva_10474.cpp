int N, Q;

int TestNum;
int main(){
  while (cin >> N >> Q){
    if (N == 0 && Q == 0){
      break;
    }
    cout << "CASE# " << ++TestNum << ":" << endl;
    vector<int> a(N);
    for (int i = 0; i < N; i++){
      cin >> a[i];
    }
    sort(a.begin(), a.end());
    std::map<int, int> pos;
    for (int i = 1; i <= N; i++){
      int n = a[i - 1];
      if (!pos.count(n)) pos[n] = i;
    }
    while (Q--){
      int n;
      cin >> n;
      if (pos.count(n)){
        cout << n << " found at " << pos[n] << endl;
      }else{
        cout << n << " not found" << endl;
      }
    }
  }
  return 0;
}
