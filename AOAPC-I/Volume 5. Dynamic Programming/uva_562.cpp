int n;
int m;
int coins[128];
bool available[512 * 128];


int main(){
  cin >> n;
  while (n--){
    cin >> m;
    for (int i = 0; i < m; i++){
      cin >> coins[i];
    }
    int total = accumulate(coins, coins + m, 0);
    memset(available, 0, sizeof(available));
    available[0] = true;
    for (int i = 0; i < m; i++){
      for (int j = total; j >= 0; j--){
        if (j - coins[i] >= 0){
          available[j] |= available[j - coins[i]];
        }
      }
    }
    int l = total / 2;
    while (!available[l]){
      l--;
    }
    printf("%d\n", abs(total - l - l));
  }
}

