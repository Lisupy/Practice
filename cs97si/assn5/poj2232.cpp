int N;
int solve(string s){
  map<char, int> cnt;
  for (size_t i = 0; i < s.size(); i++){
    cnt[s[i]]++;
  }
  if (cnt.size() == 3){
    return s.size();
  }
  if (cnt.size() == 1){
    return cnt.begin()->second;
  }
  if (cnt['C'] == 0){
    return cnt['S'];
  }
  if (cnt['F'] == 0){
    return cnt['C'];
  }
  if (cnt['S'] == 0){
    return cnt['F'];
  }
  return 0;
}
int main(){
  while (scanf("%d", &N) != EOF){
    string s;
    for (int i = 0; i < N; i++){
      string t;
      cin >> t;
      s += t;
    }
    cout << solve(s) << endl;
  }
}

  
