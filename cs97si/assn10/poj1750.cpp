int main(){
  char buf1[1024];
  char buf2[1024];
  char* word = buf1;
  char* lastWord = buf2;
  int wordCnt = 0;
  int spaceCnt = 0;
  while (scanf("%s", word) != EOF){
    if (wordCnt++ == 0){
      printf("%s\n", word);
    }else{
      int matchLen = 0;
      for (matchLen = 0; word[matchLen] && lastWord[matchLen]; matchLen++){
        if (word[matchLen] != lastWord[matchLen]){
          break;
        }
      }
      if (matchLen > spaceCnt) spaceCnt++;
      else spaceCnt = matchLen;
      printf("%*s", spaceCnt, "");
      printf("%s\n", word);
    }
    swap(word, lastWord);
  }
}

