const int MAXN = 128 * 1024;

int p[MAXN];
void p_init(){
  for (int i = 0; i < MAXN; i++) p[i] = i;
}
int find(int u){
  int root = u;
  while (root != p[root]) root = p[root];
  while (u != root) {int v = p[u]; p[u] = root; u = v; };
  return root;
}

int TestNum;
int main(){
  int cnt = 0;
  int x, y;
  p_init();
  while (cin >> x) {
    if (x == -1){
      cout << cnt << endl;
      cnt = 0;
      p_init();
    }else{
      cin >> y;
      int px = find(x);
      int py = find(y);
      if (px == py) cnt++;
      else p[px] = py;
    }
  }
}

