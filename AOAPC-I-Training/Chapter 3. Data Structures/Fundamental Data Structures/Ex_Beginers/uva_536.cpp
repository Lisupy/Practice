string get_post_order(string pre_order, string in_order){
  //cout << pre_order << ", " << in_order << endl;
  if (pre_order.size() == 0) return "";
  char root = pre_order[0];
  int lsz = in_order.find(root);
  //cout << lsz << endl;
  //string rsz = pre_order.size() - lsz - 1;
  return get_post_order(pre_order.substr(1, lsz), in_order.substr(0, lsz)) + 
      get_post_order(pre_order.substr(lsz + 1), in_order.substr(lsz + 1)) + root;
}
