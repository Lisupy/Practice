int valid(int k, int t){
  //cout << k << ", " << t << endl;
  int s = 0;
  int len = 2 * k;
  while (len > k){
    s = (s + t - 1) % len;
    //cout << s << endl;
    //cout << " " << len << endl;
    if (s < k) return false;
    len--;
  }
  return true;
}
